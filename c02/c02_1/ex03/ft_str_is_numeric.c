int	ft_str_is_numeric(char *str)
{
	int	i;
	int	ascii;

	i = 0;
	while (str[i] != '\0')
	{
		ascii = str[i] - 0;
		if ((str[i] < 48 || str[i] > 57))
		{
			return (0);
		}
		i++;
	}
	return (1);
}
