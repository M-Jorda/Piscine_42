void	ft_sort_int_tab(int *tab, int size)
{
	int	i;
	int	j;
	int	value;

	i = 0;
	while (i < size)
	{
		j = 0;
		while (j < size)
		{
			if (tab[j] > tab[j + 1])
			{
				value = tab[j];
				tab[j] = tab[j + 1];
				tab[j + 1] = value;
			}
			j++;
		}
		i++;
	}
}
