int	ft_sqrt(int nb)
{
	int	i;

	if (nb <= 0)
		return (0);
	i = 1;
	while ((i * i < nb) && (i < 46340))
	{
		i++;
	}
	if (i * i == nb)
		return (i);
	return (0);
}
